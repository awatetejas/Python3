### Question 3: Arithmetic Operations
'''
**Description:** Create a program to compute the sum, difference, product, and division of two user-provided integers.
**Test Case:** For inputs 8 and 4, the output should include: `Sum: 12, Difference: 4, Product: 32, Division: 2.0`.
'''

a = int(input('Enter first number: '))
b = int(input('Enter first number: '))

print(f'a: {a} b: {b}')

print('Addition')
print('------')
print(a+b)
print('------')

print('Subtraction')
print('------')
print(a-b)
print('------')

print('Multipication')
print('------')
print(a*b)
print('------')

print('Division')
print('------')
print(a/b)
print('------')

print('Floor Division')
print('------')
print(a//b)
print('------')

print('Modulus')
print('------')
print(a%b)
print('------')

print('Exponentiation')
print('------')
print(a**b)
